    private void salir() {
        if (guardado) {
            this.dispose();
        } else {
            //Vamos a preguntar al usuario si esta seguro de salir?
            int resultado = JOptionPane.showConfirmDialog(this, "¿Está seguro de salir?",
                    "Confirmar salida", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            //comprobamos si ha elegido el boton SI
            if (resultado == JOptionPane.YES_OPTION) {
                this.dispose();
            }
        }
    }
