    private void guardar() {
        // Abrimos una ventana de selección de archivos (JFileChoose)
        JFileChooser ventana = new JFileChooser();
        ventana.showSaveDialog(this);
        // preguntamos a la ventana el archivo elegido por el usuario.
        File archivoSeleccionado = ventana.getSelectedFile();

        //Comprobaos si el usuario ha elegido algo
        if (archivoSeleccionado != null) {
            String texto = txtTexto.getText();
            try (PrintWriter p = new PrintWriter(archivoSeleccionado)) {
                p.println(texto);
                guardado = true;
            } catch (IOException error) {
                //si falla, mostramos ventana emergente
                JOptionPane.showMessageDialog(this, error.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
