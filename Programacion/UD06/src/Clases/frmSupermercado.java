    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        // TODO add your handling code here:
        List<Producto> lista = lstProductos.getSelectedValuesList();
        int precioTotal = 0;
        for (Producto i:lista){
            precioTotal+=i.precio();
            //aprovechamos el bucle para borrar el producto una vez que ya se su precio
            productos.removeElement(i);
        }
        JOptionPane.showMessageDialog(this, "El precio total es de : "+precioTotal,"COmpra Realizada",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnComprarActionPerformed

