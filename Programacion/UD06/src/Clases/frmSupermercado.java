    private void Marcas(){
        Set<String> setM = new HashSet<>();
        for(int i = 0; i<productos.size();i++){
            //cmbMarca.addItem(productos.get(i).marca());
            setM.add(productos.get(i).marca());    
        }
     
        for(String i : setM){
            cmbMarca.addItem(i);           
         }
    }
