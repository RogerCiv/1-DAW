    private void btnCargarImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarImagenActionPerformed
        // Abrimos una ventana de selecci칩n de archivos (JFileChoose)
        JFileChooser ventana = new JFileChooser();
        ventana.showSaveDialog(this);
        // preguntamos a la ventana el archivo elegido por el usuario.
        File archivoSeleccionado = ventana.getSelectedFile();
        if(archivoSeleccionado != null){
           try {
          
            // TODO add your handling code here:
            BufferedImage img = ImageIO.read(new File(archivoSeleccionado.getPath()));
            ImageIcon icon = new ImageIcon(img);
            lblImagen.setIcon(icon);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage()
            ,"Error al cargar",JOptionPane.ERROR_MESSAGE);
             } 
        }else{
            JOptionPane.showMessageDialog(this, "No se seleccion칩 ningun archivo"
            ,"Error",JOptionPane.ERROR_MESSAGE);
        }
  
    }//GEN-LAST:event_btnCargarImagenActionPerformed

